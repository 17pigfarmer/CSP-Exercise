#include <iostream>
#include <vector>
using namespace std;
struct A {
	float min;
	float max;
	float rate;
};

int main(int argc, char** argv) {
	int sa;
	cin >> sa;
	vector<A> As;
	A _3500 = { 0,3500,0 };
	A _5000 = { 3500,3500 + 1500 * 0.97,0.03 };
	A _8000 = { 3500 + 1500 * 0.97 ,3500 + 1500 * 0.97 + 3000 * 0.90,0.1 };
	A _12500 = { 3500 + 1500 * 0.97 + 3000 * 0.90 ,3500 + 1500 * 0.97 + 3000 * 0.90 + 4500 * 0.80,0.2 };
	A _38500 = { 3500 + 1500 * 0.97 + 3000 * 0.90 + 4500 * 0.80 ,3500 + 1500 * 0.97 + 3000 * 0.90 + 4500 * 0.80 + 26000 * 0.75,0.25 };
	A _58500 = { 3500 + 1500 * 0.97 + 3000 * 0.90 + 4500 * 0.80 + 26000 * 0.75,3500 + 1500 * 0.97 + 3000 * 0.90 + 4500 * 0.80 + 26000 * 0.75 + 20000 * 0.7,0.3 };
	A _83500 = { 3500 + 1500 * 0.97 + 3000 * 0.90 + 4500 * 0.80 + 26000 * 0.75 + 20000 * 0.7,3500 + 1500 * 0.97 + 3000 * 0.90 + 4500 * 0.80 + 26000 * 0.75 + 20000 * 0.7 + 25000 * 0.65,0.35 };
	A _100000 = { 3500 + 1500 * 0.97 + 3000 * 0.90 + 4500 * 0.80 + 26000 * 0.75 + 20000 * 0.7 + 25000 * 0.65,100000,0.45 };
	As.push_back(_3500);
	As.push_back(_5000);
	As.push_back(_8000);
	As.push_back(_12500);
	As.push_back(_38500);
	As.push_back(_58500);
	As.push_back(_83500);
	As.push_back(_100000);
	float sum= 0;
	for (auto a : As) {
		if (sa >= a.max) {
			sum += (a.max - a.min)/(1-a.rate);
			continue;
		}
		else if (sa >= a.min) {
			sum += (sa - a.min)/(1-a.rate);
		}
		else {
			continue;
		}
	}

	int x = (int)sum;
	float y = sum - (float)x;
	if (y > 0.5) {
		x++;
	}
	cout << x << endl;
	return 0;
}